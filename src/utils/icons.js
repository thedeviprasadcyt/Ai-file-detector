/**
 * Get color associated with a file category.
 * @param {string} category - The file category.
 * @returns {string} CSS color value.
 */
export function getCategoryColor(category) {
  const colors = {
    Image: '#00d2ff',
    Document: '#7c4dff',
    Audio: '#ff6b9d',
    Video: '#ffd93d',
    Archive: '#6bcb77',
    Executable: '#ff4757',
    Code: '#00d2ff',
    Data: '#a29bfe',
    Font: '#fd79a8',
    Database: '#fdcb6e',
    Unknown: '#636e72',
  };

  return colors[category] || colors.Unknown;
}

/**
 * Get the security status icon.
 * @param {string} status - Security status ('Safe', 'Suspicious', 'Potentially Dangerous').
 * @returns {string} Emoji/icon string.
 */
export function getSecurityIcon(status) {
  switch (status) {
    case 'Safe': return '🛡️';
    case 'Suspicious': return '⚠️';
    case 'Potentially Dangerous': return '🚨';
    default: return '❓';
  }
}
